package com.aim.training.java.interfaces;

public class Person {

	private String name;
	private int age;
	private String place;
	public String getName()
	{
		return name;
	}
	public int getAge() 
	{
		return age;
		
	}
	
	public String getPlace()
	{
		return place;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public void setAge(int age)
	{
		this.age =age;
	}
		
	public void setPlace(String place)
	{
		this.place = place;
	}
	
}
