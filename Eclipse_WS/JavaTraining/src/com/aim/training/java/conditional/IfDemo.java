package com.aim.training.java.conditional;

public class IfDemo {

	
	public static void main(String[] args) {
		int a =10, b = 10;
		
		if(a>b)
		{
			System.out.println("first number is greater");
		}
		else if(b>a)
		{
			System.out.println("second number is greater");
		}
		else {
			System.out.println("Both are equal");
		}
		
		
	}
}
